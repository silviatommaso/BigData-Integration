import pandas as pd
import os

def load_movies_csv(path):
    
    df = pd.read_csv(path)
    return df.convert_dtypes()

##################################################################################################################################################################################################################################################################################################################################################
# path existence checker
##################################################################################################################################################################################################################################################################################################################################################

def path_check(files, next_step=None):

    for file in files:

        if not os.path.exists(file):

            if next_step:
                print(f"Error: {file} not found, execute {next_step} step first")
            else:
                print(f"Error: {file} not found")

            exit()

def subpath_check(files, indexes, step):

    to_check = [[f[i] for i in indexes] + [f[-1]] for f in files.values()]
    
    for file in to_check[:(step-1)]:
        path_check(file)

